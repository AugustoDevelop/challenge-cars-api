package com.api.util;

/**
 * Enum representing the status of a user account.
 *
 * <p>This enum defines the possible states a user can be in, such as active or inactive.
 */
public enum UserStatus {
    /**
     * Indicates that the user account is active and fully functional.
     */
    ACTIVE,

    /**
     * Indicates that the user account is inactive, possibly due to deletion or suspension.
     */
    INACTIVE
}
